    private void lib_tableMouseClicked(java.awt.event.MouseEvent evt) {// GEN-FIRST:event_lib_tableMouseClicked
        selectedRow = lib_table.getSelectedRow();
        if (lib_table.getSelectedRowCount() > 0) {
            addLib.setEnabled(false);
            deleteLib.setEnabled(true);
            editLib.setEnabled(true);
            id = (int) lib_table.getValueAt(selectedRow, 0);
            String sqlusername, sqlpassword;
            try {
                con = DriverManager.getConnection(url, user, pass);
                pst = con.prepareStatement("SELECT username,password FROM librarians WHERE lib_id=?");
                pst.setInt(1, id);
                rt = pst.executeQuery();
                rt.next();
                sqlusername = rt.getString("username");
                sqlpassword = rt.getString("password");
                username.setText(sqlusername);
                password.setText(sqlpassword);
                name.setText(lib_table.getValueAt(selectedRow, 1).toString());
                phone.setText(lib_table.getValueAt(selectedRow, 3).toString());
                address.setText(lib_table.getValueAt(selectedRow, 4).toString());
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        } else {
            clearData();
        }
    }// GEN-LAST:event_lib_tableMouseClicked

