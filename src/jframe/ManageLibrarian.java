    public static String getSecurePassword(String password) {
        String generatedPassword = null;
        try {
            MessageDigest md = MessageDigest.getInstance("SHA-256");
            byte[] bytes = md.digest(password.getBytes());
            StringBuilder sb = new StringBuilder();
            for (byte b : bytes) {
                sb.append(String.format("%02x", b));
            }
            generatedPassword = sb.toString();
        } catch (NoSuchAlgorithmException e) {
            e.printStackTrace();
        }
        return generatedPassword;
    }

    private void addLibActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_addLibActionPerformed
        if (name.getText().isEmpty() || phone.getText().isEmpty() || username.getText().isEmpty()
                || password.getText().isEmpty() || address.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Fill all the data before submission.");
        } else {
            String hashedPassword = getSecurePassword(password.getText());
            try {
                con = DriverManager.getConnection(url, user, pass);
                pst = con.prepareStatement(
                        "INSERT INTO librarians (name,username, password, phone, address) VALUES (?,?,?,?,?)");
                pst.setString(1, name.getText());
                pst.setString(2, username.getText());
                pst.setString(3, hashedPassword);
                pst.setString(4, phone.getText());
                pst.setString(5, address.getText());
                pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "Librarian has been successfully added.");
                showLib();
                clearData();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        }
    }// GEN-LAST:event_addLibActionPerformed

